const { Schema, model } = require('mongoose');

const pledgeSchema = new Schema({
  title: {
    type: String,
    required: true,
    trim: true,
  },
  description: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Pledge = model('Pledge', pledgeSchema);

module.exports = Pledge;
